// Generates src/web/lightdark-fallback.css: static color fallbacks for
// engines without CSS light-dark() (WKWebView below Safari 17.5, e.g. macOS
// Big Sur). Extracts every declaration in viewer.css/nobloat.css that uses
// light-dark(), keeps its full selector/at-rule nesting context, and emits a
// light-resolved copy plus a dark-media-resolved copy inside
// `@supports not (color: light-dark(...))` — modern engines skip the whole
// block, old ones apply it. Runtime <style> injection is NOT an option here:
// Tauri's CSP carries style hashes, which void 'unsafe-inline'.
//
// Re-run after upgrading pdf.js (viewer.css) or editing nobloat.css colors:
//   node scripts/gen-lightdark-fallback.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const FN = 'light-dark(';
const web = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'web');

const resolve = (css, dark) => {
  let i;
  while ((i = css.indexOf(FN)) !== -1) {
    let depth = 1;
    let comma = -1;
    let j = i + FN.length;
    for (; j < css.length && depth > 0; j++) {
      const ch = css[j];
      if (ch === '(') depth++;
      else if (ch === ')') depth--;
      else if (ch === ',' && depth === 1 && comma === -1) comma = j;
    }
    if (comma === -1 || depth > 0) throw new Error('malformed light-dark()');
    const value = dark ? css.slice(comma + 1, j - 1) : css.slice(i + FN.length, comma);
    css = css.slice(0, i) + value.trim() + css.slice(j);
  }
  return css;
};

const parse = (css) => {
  let i = 0;
  const n = css.length;
  const parseBlock = (prelude) => {
    const node = { prelude, decls: [], children: [] };
    let chunk = '';
    while (i < n) {
      const ch = css[i];
      if (ch === '/' && css[i + 1] === '*') {
        i += 2;
        while (i < n && !(css[i] === '*' && css[i + 1] === '/')) i++;
        i += 2;
      } else if (ch === '"' || ch === "'") {
        chunk += ch;
        i++;
        while (i < n) {
          chunk += css[i];
          if (css[i] === '\\') {
            chunk += css[i + 1] ?? '';
            i += 2;
          } else if (css[i] === ch) {
            i++;
            break;
          } else i++;
        }
      } else if (ch === '{') {
        i++;
        node.children.push(parseBlock(chunk.trim()));
        chunk = '';
      } else if (ch === '}') {
        i++;
        break;
      } else if (ch === ';') {
        i++;
        if (chunk.trim()) node.decls.push(chunk.trim());
        chunk = '';
      } else {
        chunk += ch;
        i++;
      }
    }
    if (chunk.trim()) node.decls.push(chunk.trim());
    return node;
  };
  return parseBlock(null);
};

const extract = (node) => {
  const decls = node.decls.filter((d) => d.includes(FN));
  const children = node.children.map(extract).filter(Boolean);
  if (!decls.length && !children.length) return '';
  const body = decls.map((d) => d + ';').join('') + children.join('');
  return node.prelude === null ? body : `${node.prelude}{${body}}`;
};

let patch = '';
let total = 0;
for (const f of ['viewer.css', 'nobloat.css']) {
  const src = readFileSync(join(web, f), 'utf8');
  total += src.split(FN).length - 1;
  patch += extract(parse(src));
}
if (patch.split(FN).length - 1 !== total) {
  throw new Error('extraction lost light-dark declarations');
}

const out =
  '/* GENERATED by scripts/gen-lightdark-fallback.mjs — do not edit by hand.\n' +
  '   Static fallbacks for engines without CSS light-dark(); see script header.\n' +
  `   Covers ${total} light-dark() declarations from viewer.css + nobloat.css. */\n` +
  '@supports not (color: light-dark(#000, #fff)) {\n' +
  resolve(patch, false) +
  '\n@media (prefers-color-scheme: dark) {\n' +
  resolve(patch, true) +
  '\n}\n}\n';

writeFileSync(join(web, 'lightdark-fallback.css'), out);
console.log(`wrote src/web/lightdark-fallback.css (${out.length} bytes, ${total} declarations x2 schemes)`);
